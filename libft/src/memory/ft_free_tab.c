#include "../../libft.h"

void	ft_free_tab(void **s)
{
	int	i;

	i = -1;
	if (!s)
		return ;
	if (!s[0])
	{
		free(s);
		s = NULL;
		return ;
	}
	while (s[++i])
		free(s[i]);
	if (s)
	{
		free(s);
		s = NULL;
	}
}
